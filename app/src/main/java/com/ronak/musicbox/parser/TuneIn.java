public class TuneIn {
   private String base;
   private String base_m3u;
   private String base_xspf;

   public String getBase() {
	  return base;
   }

   public void setBase(String base) {
	  this.base = base;
   }

   public String getBase_m3u() {
	  return base_m3u;
   }

   public void setBase_m3u(String base_m3u) {
	  this.base_m3u = base_m3u;
   }

   public String getBase_xspf() {
	  return base_xspf;
   }

   public void setBase_xspf(String base_xspf) {
	  this.base_xspf = base_xspf;
   }
}
